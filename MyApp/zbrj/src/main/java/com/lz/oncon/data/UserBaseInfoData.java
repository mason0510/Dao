package com.lz.oncon.data;

public class UserBaseInfoData {

	public String name;
	public String sex;
	public String direction;
	public String company;
	public String jobtitle;
	public String workaddress;
	public String email;
	public String wechat;
	public String qq;

	public UserBaseInfoData(String name, String sex, String direction, String company, String jobtitle, String workaddress, String email,
			String wechat, String qq) {
		this.name = name;
		this.sex = sex;
		this.direction = direction;
		this.company = company;
		this.jobtitle = jobtitle;
		this.workaddress = workaddress;
		this.email = email;
		this.wechat = wechat;
		this.qq = qq;
	}

}
